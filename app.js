function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function isAnswerCorrect(step, selectedAnswers) {
  if (!step.quiz) {
    return false;
  }

  const selected = Array.isArray(selectedAnswers) ? selectedAnswers : [];
  const expected = new Set(step.quiz.correctAnswers || []);

  if (step.quiz.selectionMode === "single") {
    return selected.length === 1 && expected.has(selected[0]);
  }

  const selectedSet = new Set(selected);
  return selectedSet.size === expected.size && [...selectedSet].every((answer) => expected.has(answer));
}

function launchConfetti(container) {
  const existing = document.querySelector(".confetti-layer");
  if (existing) {
    existing.remove();
  }

  const layer = document.createElement("div");
  layer.className = "confetti-layer";
  layer.setAttribute("aria-hidden", "true");

  const rect = container.getBoundingClientRect();
  const originX = window.innerWidth * 0.5;
  const originY = Math.max(84, rect.top + Math.min(56, rect.height * 0.18));

  const colors = ["#0b415f", "#2f728e", "#276c6f", "#369599", "#b7791f", "#d467d1", "#8bc2d8"];
  const pieceCount = 160;

  for (let i = 0; i < pieceCount; i += 1) {
    const piece = document.createElement("span");
    piece.className = "confetti-piece";

    const dx = Math.round((Math.random() * 2 - 1) * 640);
    const dy = Math.round(260 + Math.random() * 520);
    const rot = Math.round((Math.random() * 2 - 1) * 840);
    const wave = i % 2;
    const delay = (wave * 0.14 + Math.random() * 0.28).toFixed(3);
    const duration = (2.4 + Math.random() * 1.4).toFixed(2);
    const size = 8 + Math.round(Math.random() * 10);
    const hue = colors[i % colors.length];
    const offsetX = (Math.random() * 140 - 70).toFixed(2);
    const offsetY = (Math.random() * 40 - 20).toFixed(2);

    piece.style.setProperty("--dx", `${dx}px`);
    piece.style.setProperty("--dy", `${dy}px`);
    piece.style.setProperty("--rot", `${rot}deg`);
    piece.style.setProperty("--delay", `${delay}s`);
    piece.style.setProperty("--dur", `${duration}s`);
    piece.style.setProperty("--size", `${size}px`);
    piece.style.setProperty("--color", hue);
    piece.style.left = `${originX + Number(offsetX)}px`;
    piece.style.top = `${originY + Number(offsetY)}px`;

    layer.append(piece);
  }

  document.body.append(layer);
  setTimeout(() => layer.remove(), 4200);
}