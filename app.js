function computeTruckTop(stepCount, activeIndex) {
  const start = 42;
  const end = Math.max(42, stepCount * 108 - 4);

  if (stepCount <= 1) {
    return start;
  }

  const ratio = Math.min(activeIndex, stepCount - 1) / (stepCount - 1);
  return start + ratio * (end - start);
}

function normalizeContainerNumber(value) {
  return value.replace(/\s+/g, "").toUpperCase();
}

function isValidContainerNumber(value) {
  if (!/^[A-Z]{4}\d{7}$/.test(value)) {
    return false;
  }

  const code = value.slice(0, 10);
  const checkDigit = Number(value[10]);
  return calculateIso6346CheckDigit(code) === checkDigit;
}

function calculateIso6346CheckDigit(code) {
  const letterValues = {
    A: 10,
    B: 12,
    C: 13,
    D: 14,
    E: 15,
    F: 16,
    G: 17,
    H: 18,
    I: 19,
    J: 20,
    K: 21,
    L: 23,
    M: 24,
    N: 25,
    O: 26,
    P: 27,
    Q: 28,
    R: 29,
    S: 30,
    T: 31,
    U: 32,
    V: 34,
    W: 35,
    X: 36,
    Y: 37,
    Z: 38
  };

  const total = code.split("").reduce((sum, character, index) => {
    const value = /\d/.test(character) ? Number(character) : letterValues[character];
    return sum + value * 2 ** index;
  }, 0);
  const remainder = total % 11;
  return remainder % 10;
}

